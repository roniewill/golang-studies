package main

import "fmt"

func main() {
	/*
		relational assignment
		==  equal to
		!= different than
		>  bigger then
		<  less than
		>= greater than or equal to
		<= less than or equal to
	*/
	num1 := 5
	num2 := 10
	fmt.Println("=", num1 == num2)
	fmt.Println("!=", num1 != num2)
	fmt.Println(">", num1 > num2)
	fmt.Println("<", num1 < num2)
	fmt.Println(">=", num1 >= num2)
	fmt.Println("<=", num1 <= num2)
}
